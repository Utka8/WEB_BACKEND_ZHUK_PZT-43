<h2>4. Пользовательские функции</h2>
<?php

// Обычная функция с параметром.
// Возвращает строку-приветствие.
function greet($name) {
    return "Привет, $name!";
}
echo greet("Анастасия") . "<br>";


// Функция с параметром по умолчанию.
// Если второй аргумент не передан — используется 10.
function sum($a, $b = 10) {
    return $a + $b;
}
echo "sum(5) = " . sum(5) . "<br>";


// Стрелочная функция (аналог лямбда-выражения).
// Короткая форма записи функции.
$mul = fn($x, $y) => $x * $y;

echo "Стрелочная функция: " . $mul(3, 4) . "<br>";

?>
